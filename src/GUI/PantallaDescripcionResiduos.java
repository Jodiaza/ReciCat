    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
        Gato gato = new Gato();
        PantallaInicio pantallaInicio = new PantallaInicio(true,gato);
        PantallaDescripcionResiduos.setCaida(true);
        pantallaInicio.getjPanel1().setSize(370,620);
        pantallaInicio.getjPanel1().setLocation(0,0);
        removeAll();
        PantallaInicio.getjLabel1().setVisible(true);
        add(pantallaInicio.getjPanel1(),BorderLayout.CENTER);
        switch(PantallaDeResiduos.getImagenResiduo()){
                case 1:
                    PantallaInicio.getjLabel1().setIcon(Pantalla.escalar(
                    PantallaDeResiduos.catalogo.get("Plasticos").getImagen(),80, 80));
                    gato.comer(PantallaDeResiduos.catalogo.get("Plasticos"));
                    break;
                case 2:
                    PantallaInicio.getjLabel1().setIcon(Pantalla.escalar(
                    PantallaDeResiduos.catalogo.get("Papel").getImagen(),80, 80));
                    gato.comer(PantallaDeResiduos.catalogo.get("Papel"));
                    break;
                case 3:
                    PantallaInicio.getjLabel1().setIcon(Pantalla.escalar(
                    PantallaDeResiduos.catalogo.get("Ordinarios").getImagen(),80, 80));
                    gato.comer(PantallaDeResiduos.catalogo.get("Ordinarios"));
                    break;
                case 4:
                    PantallaInicio.getjLabel1().setIcon(Pantalla.escalar(
                    PantallaDeResiduos.catalogo.get("Organicos").getImagen(),80, 80));
                    gato.comer(PantallaDeResiduos.catalogo.get("Organicos"));
                    break;
                case 5:
                    PantallaInicio.getjLabel1().setIcon(Pantalla.escalar(
                    PantallaDeResiduos.catalogo.get("Carton").getImagen(),80, 80));
                    gato.comer(PantallaDeResiduos.catalogo.get("Carton"));
                    break;
                case 6:
                    PantallaInicio.getjLabel1().setIcon(Pantalla.escalar(
                    PantallaDeResiduos.catalogo.get("Vidrios").getImagen(),80, 80));
                    gato.comer(PantallaDeResiduos.catalogo.get("Vidrios"));
                    break;
                case 7:
                    PantallaInicio.getjLabel1().setIcon(Pantalla.escalar(
                    PantallaDeResiduos.catalogo.get("Peligrosos").getImagen(),80, 80));
                    gato.comer(PantallaDeResiduos.catalogo.get("Peligrosos"));
                    break;
            }
        gato.getT().start();
        revalidate();
        repaint();   

    }//GEN-LAST:event_btnOkActionPerformed

