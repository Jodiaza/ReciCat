    public void salir(){
        Gato gato = new Gato();
        PantallaInicio pantallaInicio = new PantallaInicio(true,gato);
        pantallaInicio.getjPanel1().setSize(370,620);
        pantallaInicio.getjPanel1().setLocation(0, 0);
        removeAll();
        add(pantallaInicio.getjPanel1(),BorderLayout.CENTER);
        gato.getT().start();
        revalidate();
        repaint();
    }
    
    public void retroceder(){
        PantallaDeResiduos residuos = new PantallaDeResiduos();
        residuos.setSize(370,620);
        residuos.setLocation(0, 0);
        removeAll();
        add(residuos,BorderLayout.CENTER);
        revalidate();
        repaint();
    }

